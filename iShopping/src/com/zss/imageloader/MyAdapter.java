package com.zss.imageloader;

import java.util.ArrayList;
import java.util.LinkedList;
import java.util.List;

import android.content.Context;
import android.graphics.Color;
import android.util.Log;
import android.view.View;
import android.view.View.OnClickListener;
import android.widget.ImageView;
import android.widget.Toast;

import com.dz4.ishopping.R;
import com.zss.utils.CommonAdapter;

public class MyAdapter extends CommonAdapter<String> {

	/**
	 * 用户选择的图片，存储为图片的完整路径
	 */
	public static List<String> mSelectedImage = new LinkedList<String>();
	public static ArrayList<String> imgItem = new ArrayList<String>();
	public static ArrayList<String> imgDirPath = new ArrayList<String>();

	public static int flag = 0;
	/**
	 * 文件夹路径
	 */
	private String mDirPath;
	private Context context;

	public MyAdapter(Context context, List<String> mDatas, int itemLayoutId,
			String dirPath) {
		super(context, mDatas, itemLayoutId);
		this.mDirPath = dirPath;
		this.context = context;
	}

	@Override
	public void convert(final com.zss.utils.ViewHolder helper, final String item) {

		// 设置no_pic
		helper.setImageResource(R.id.id_item_image, R.drawable.zss_pictures_no);
		// 设置no_selected
		helper.setImageResource(R.id.id_item_select,
				R.drawable.zss_picture_unselected);
		// 设置图片
		helper.setImageByUrl(R.id.id_item_image, mDirPath + "/" + item);

		final ImageView mImageView = helper.getView(R.id.id_item_image);
		final ImageView mSelect = helper.getView(R.id.id_item_select);

		// 图片 滤镜 ， 可使图片变暗效果
		mImageView.setColorFilter(null);
		// 设置ImageView的点击事件
		mImageView.setOnClickListener(new OnClickListener() {
			// 选择，则将图片变暗，反之则反之
			@Override
			public void onClick(View v) {

			 

					// 已经选择过该图片
					if (mSelectedImage.contains(mDirPath + "/" + item)) {

						mSelectedImage.remove(mDirPath + "/" + item);
						mSelect.setImageResource(R.drawable.zss_picture_unselected);
						mImageView.setColorFilter(null);

						imgItem.remove(item);
						imgDirPath.remove(mDirPath);
					} else{
					// 未选择该图片
					// 刚开始图片是没被点击的 ， 当图片点击时，向 mSelectedImage 添加 path
					 
						if(imgItem.size() < 9){
							
						 
						mSelectedImage.add(mDirPath + "/" + item);
						Log.i("iiiiiiiiiyyyyyyy", item);
						mSelect.setImageResource(R.drawable.zss_pictures_selected);
						mImageView.setColorFilter(Color.parseColor("#77000000"));

						imgItem.add(item);
						imgDirPath.add(mDirPath);

						}else{
							Toast.makeText(context, "图片选择不能超过9张", Toast.LENGTH_SHORT).show();
							return;
						}
					}
			}
		});

		/**
		 * 已经选择过的图片，显示出选择过的效果
		 */
		if (mSelectedImage.contains(mDirPath + "/" + item)) {

			mSelect.setImageResource(R.drawable.zss_pictures_selected);
			mImageView.setColorFilter(Color.parseColor("#77000000"));
		}

	}

	public ArrayList<String> returnImgItem() {
		return imgItem;
	}

	public ArrayList<String> returnImgDirPath() {
		return imgDirPath;
	}

}
